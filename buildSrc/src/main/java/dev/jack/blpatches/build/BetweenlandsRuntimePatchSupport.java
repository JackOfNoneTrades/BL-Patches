package dev.jack.blpatches.build;

import java.io.ByteArrayOutputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.StandardCopyOption;
import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;
import java.util.Enumeration;
import java.util.LinkedHashSet;
import java.util.Map;
import java.util.Properties;
import java.util.Set;
import java.util.TreeMap;
import java.util.TreeSet;
import java.util.zip.CRC32;
import java.util.zip.ZipEntry;
import java.util.zip.ZipFile;
import java.util.zip.ZipOutputStream;

import org.gradle.api.GradleException;
import org.gradle.api.Project;
import org.gradle.api.logging.Logger;

import io.sigpipe.jbsdiff.Diff;

public final class BetweenlandsRuntimePatchSupport {

    private static final String PATCH_ROOT = "patch-resources";
    private static final String PATCH_RESOURCE = "blpatches/thebetweenlands-jar.patch";
    private static final String PATCH_METADATA_RESOURCE = "blpatches/thebetweenlands-jar-patch.properties";
    private static final String ARTIFACTS_ROOT = "artifacts";
    private static final String RUNTIME_TARGET_JAR_NAME = "TheBetweenlands-runtime-target.jar";

    private BetweenlandsRuntimePatchSupport() {}

    public static void generate(Project project, Path deobfWorkspaceJar, Path reobfWorkspaceJar, Path outputDirectory) {
        BetweenlandsPatchSettings settings = BetweenlandsPatchSettings.from(project);
        Logger logger = project.getLogger();
        BetweenlandsWorkspaceSupport.ensureWorkspaceExists(project);
        BetweenlandsWorkspaceSupport.downloadOfficialJar(project);

        Path patchResourcesDir = outputDirectory.resolve(PATCH_ROOT);
        Path artifactsDir = outputDirectory.resolve(ARTIFACTS_ROOT);
        Path runtimeTargetJar = artifactsDir.resolve(RUNTIME_TARGET_JAR_NAME);

        BetweenlandsWorkspaceSupport.deleteRecursively(outputDirectory);

        try {
            Files.createDirectories(patchResourcesDir.resolve("blpatches"));
            Files.createDirectories(artifactsDir);

            BetweenlandsWorkspaceDiff workspaceDiff = BetweenlandsWorkspaceSupport.diffWorkspace(project);
            RuntimeJarResult runtimeJarResult = hasWorkspaceChanges(workspaceDiff)
                    ? buildRuntimeTargetJar(settings.getOfficialJarPath(), reobfWorkspaceJar, runtimeTargetJar, workspaceDiff)
                    : copyOfficialJar(settings.getOfficialJarPath(), runtimeTargetJar);
            byte[] oldBytes = Files.readAllBytes(settings.getOfficialJarPath());
            byte[] newBytes = Files.readAllBytes(runtimeTargetJar);
            byte[] patchBytes = diff(oldBytes, newBytes);
            Files.write(patchResourcesDir.resolve(PATCH_RESOURCE), patchBytes);

            Properties patchMetadata = new Properties();
            patchMetadata.setProperty("officialJarName", settings.getOfficialJarName());
            patchMetadata.setProperty("originalSha256", sha256(oldBytes));
            patchMetadata.setProperty("patchedSha256", sha256(newBytes));
            patchMetadata.setProperty("devWorkspaceJarName", deobfWorkspaceJar.getFileName().toString());
            patchMetadata.setProperty("runtimeTargetJarName", runtimeTargetJar.getFileName().toString());
            try (OutputStream output = Files.newOutputStream(patchResourcesDir.resolve(PATCH_METADATA_RESOURCE))) {
                patchMetadata.store(output, "Generated by generateBetweenlandsRuntimeArtifacts");
            }

            logger.lifecycle(
                    "Generated a whole-jar The Betweenlands patch ({} bytes) with {} class overlay(s), {} resource overlay(s), and {} deletion(s)",
                    patchBytes.length,
                    runtimeJarResult.overlaidClassEntries,
                    runtimeJarResult.overlaidResourceEntries,
                    runtimeJarResult.deletedEntries);
        } catch (IOException exception) {
            throw new GradleException("Failed to generate runtime The Betweenlands patch artifacts", exception);
        }
    }

    private static RuntimeJarResult buildRuntimeTargetJar(
            Path officialJarPath,
            Path reobfWorkspaceJarPath,
            Path runtimeTargetJarPath,
            BetweenlandsWorkspaceDiff workspaceDiff) throws IOException {
        Map<String, ZipEntryWithBytes> reobfEntries = loadZipEntries(reobfWorkspaceJarPath);
        Set<String> classOverlays = workspaceDiff.getOverlayClassPrefixes();
        Set<String> deletedClasses = workspaceDiff.getDeletedClassPrefixes();
        Set<String> resourceOverlays = workspaceDiff.getOverlayResources();
        Set<String> deletedResources = workspaceDiff.getDeletedResources();
        Set<String> writtenEntries = new LinkedHashSet<>();

        int overlaidClassEntries = 0;
        int overlaidResourceEntries = 0;
        int deletedEntries = 0;

        try (ZipFile officialJar = new ZipFile(officialJarPath.toFile());
                ZipOutputStream output = new ZipOutputStream(Files.newOutputStream(runtimeTargetJarPath))) {
            Enumeration<? extends ZipEntry> entries = officialJar.entries();
            while (entries.hasMoreElements()) {
                ZipEntry officialEntry = entries.nextElement();
                String entryPath = officialEntry.getName();
                if (officialEntry.isDirectory()) {
                    continue;
                }

                if (isDeleted(entryPath, deletedClasses, deletedResources)) {
                    deletedEntries++;
                    continue;
                }

                ZipEntryWithBytes replacement = replacementFor(entryPath, reobfEntries, classOverlays, resourceOverlays);
                if (replacement != null) {
                    writeZipEntry(output, entryPath, replacement.bytes, replacement.entry);
                    writtenEntries.add(entryPath);
                    if (entryPath.endsWith(".class")) {
                        overlaidClassEntries++;
                    } else {
                        overlaidResourceEntries++;
                    }
                    continue;
                }

                writeZipEntry(output, entryPath, readZipEntry(officialJar, officialEntry), officialEntry);
                writtenEntries.add(entryPath);
            }

            for (String resourcePath : new TreeSet<>(resourceOverlays)) {
                if (writtenEntries.contains(resourcePath)) {
                    continue;
                }
                ZipEntryWithBytes addition = reobfEntries.get(resourcePath);
                if (addition == null) {
                    continue;
                }
                writeZipEntry(output, resourcePath, addition.bytes, addition.entry);
                writtenEntries.add(resourcePath);
                overlaidResourceEntries++;
            }

            for (Map.Entry<String, ZipEntryWithBytes> entry : reobfEntries.entrySet()) {
                String entryPath = entry.getKey();
                if (writtenEntries.contains(entryPath) || !isOverlayClassEntry(entryPath, classOverlays)) {
                    continue;
                }
                writeZipEntry(output, entryPath, entry.getValue().bytes, entry.getValue().entry);
                writtenEntries.add(entryPath);
                overlaidClassEntries++;
            }
        }

        return new RuntimeJarResult(overlaidClassEntries, overlaidResourceEntries, deletedEntries);
    }

    private static RuntimeJarResult copyOfficialJar(Path officialJarPath, Path runtimeTargetJarPath) throws IOException {
        Files.copy(officialJarPath, runtimeTargetJarPath, StandardCopyOption.REPLACE_EXISTING);
        return new RuntimeJarResult(0, 0, 0);
    }

    private static Map<String, ZipEntryWithBytes> loadZipEntries(Path jarPath) throws IOException {
        Map<String, ZipEntryWithBytes> entries = new TreeMap<>();
        try (ZipFile zipFile = new ZipFile(jarPath.toFile())) {
            Enumeration<? extends ZipEntry> zipEntries = zipFile.entries();
            while (zipEntries.hasMoreElements()) {
                ZipEntry entry = zipEntries.nextElement();
                if (entry.isDirectory() || shouldIgnoreReobfEntry(entry.getName())) {
                    continue;
                }
                entries.put(entry.getName(), new ZipEntryWithBytes(copyEntry(entry), readZipEntry(zipFile, entry)));
            }
        }
        return entries;
    }

    private static byte[] readZipEntry(ZipFile zipFile, ZipEntry entry) throws IOException {
        try (InputStream input = zipFile.getInputStream(entry)) {
            return readAllBytes(input);
        }
    }

    private static byte[] readAllBytes(InputStream input) throws IOException {
        ByteArrayOutputStream output = new ByteArrayOutputStream();
        byte[] buffer = new byte[8192];
        int read;
        while ((read = input.read(buffer)) >= 0) {
            output.write(buffer, 0, read);
        }
        return output.toByteArray();
    }

    private static byte[] diff(byte[] oldBytes, byte[] newBytes) throws IOException {
        try {
            ByteArrayOutputStream output = new ByteArrayOutputStream();
            Diff.diff(oldBytes, newBytes, output);
            return output.toByteArray();
        } catch (Exception exception) {
            throw new IOException("Failed to create bspatch payload", exception);
        }
    }

    private static String sha256(byte[] bytes) {
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            digest.update(bytes);
            return BetweenlandsWorkspaceSupport.hex(digest.digest());
        } catch (NoSuchAlgorithmException exception) {
            throw new IllegalStateException("SHA-256 should always be available", exception);
        }
    }

    private static boolean shouldIgnoreReobfEntry(String entryPath) {
        return entryPath.startsWith("META-INF/");
    }

    private static boolean isDeleted(String entryPath, Set<String> deletedClasses, Set<String> deletedResources) {
        return deletedResources.contains(entryPath) || matchesAnyClassPrefix(entryPath, deletedClasses);
    }

    private static ZipEntryWithBytes replacementFor(
            String entryPath,
            Map<String, ZipEntryWithBytes> reobfEntries,
            Set<String> classOverlays,
            Set<String> resourceOverlays) {
        if (resourceOverlays.contains(entryPath)) {
            return reobfEntries.get(entryPath);
        }
        if (matchesAnyClassPrefix(entryPath, classOverlays)) {
            return reobfEntries.get(entryPath);
        }
        return null;
    }

    private static boolean isOverlayClassEntry(String entryPath, Set<String> classOverlays) {
        return matchesAnyClassPrefix(entryPath, classOverlays);
    }

    private static boolean matchesAnyClassPrefix(String entryPath, Set<String> classPrefixes) {
        for (String prefix : classPrefixes) {
            if (matchesClassPrefix(entryPath, prefix)) {
                return true;
            }
        }
        return false;
    }

    private static boolean matchesClassPrefix(String entryPath, String classPrefix) {
        return entryPath.equals(classPrefix + ".class")
                || (entryPath.startsWith(classPrefix + "$") && entryPath.endsWith(".class"));
    }

    private static boolean hasWorkspaceChanges(BetweenlandsWorkspaceDiff workspaceDiff) {
        return !workspaceDiff.getOverlayClassPrefixes().isEmpty()
                || !workspaceDiff.getDeletedClassPrefixes().isEmpty()
                || !workspaceDiff.getOverlayResources().isEmpty()
                || !workspaceDiff.getDeletedResources().isEmpty();
    }

    private static void writeZipEntry(ZipOutputStream output, String entryPath, byte[] bytes, ZipEntry template)
            throws IOException {
        ZipEntry newEntry = new ZipEntry(entryPath);
        if (template != null) {
            if (template.getComment() != null) {
                newEntry.setComment(template.getComment());
            }
            if (template.getTime() >= 0L) {
                newEntry.setTime(template.getTime());
            }
            if (template.getMethod() == ZipEntry.STORED) {
                newEntry.setMethod(ZipEntry.STORED);
                newEntry.setSize(bytes.length);
                newEntry.setCompressedSize(bytes.length);
                CRC32 crc = new CRC32();
                crc.update(bytes);
                newEntry.setCrc(crc.getValue());
            }
        }
        output.putNextEntry(newEntry);
        output.write(bytes);
        output.closeEntry();
    }

    private static ZipEntry copyEntry(ZipEntry entry) {
        ZipEntry copy = new ZipEntry(entry.getName());
        copy.setComment(entry.getComment());
        if (entry.getTime() >= 0L) {
            copy.setTime(entry.getTime());
        }
        if (entry.getMethod() == ZipEntry.STORED) {
            copy.setMethod(ZipEntry.STORED);
            copy.setSize(entry.getSize());
            copy.setCompressedSize(entry.getCompressedSize());
            copy.setCrc(entry.getCrc());
        }
        return copy;
    }

    private static final class ZipEntryWithBytes {

        private final ZipEntry entry;
        private final byte[] bytes;

        private ZipEntryWithBytes(ZipEntry entry, byte[] bytes) {
            this.entry = entry;
            this.bytes = bytes;
        }
    }

    private static final class RuntimeJarResult {

        private final int overlaidClassEntries;
        private final int overlaidResourceEntries;
        private final int deletedEntries;

        private RuntimeJarResult(int overlaidClassEntries, int overlaidResourceEntries, int deletedEntries) {
            this.overlaidClassEntries = overlaidClassEntries;
            this.overlaidResourceEntries = overlaidResourceEntries;
            this.deletedEntries = deletedEntries;
        }
    }
}
